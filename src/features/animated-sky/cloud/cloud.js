export class Cloud {
    
    
    constructor(x, y, image) {
        this.x = x;
        this.y = y;
        this.image = image;
    }

    setX(x) {
        this.x = x;
    }
    getX() {
        return this.x;
    }
    setY(y) {
        this.y = y;
    }
    getY() {
        return this.y;
    }
    setImage(image) {
        this.image = image;
    }
    getImage() {
        return this.image;
    }
}


